    private void jBueditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBueditarActionPerformed
        Usuario u;
        try {
            u = UsuarioBD.BuscarUsuario(jtfcodigousuario1.getText());
            u.setNombre(jtfnomusuario.getText());
            u.setApellido(jtfapellidoUsuario.getText());
            u.setCedula(jtfCedulausuario.getText());
            u.setUsuario(jtfUsuario.getText());
            u.setTelefono(jtfTelefonoUsuario.getText());
            u.setPasswork(jtfpassworkUsuario.getText());
            u.setTipoUsuario(jTextField1.getText());
            UsuarioBD.ActualizarUsuario(u);
            JOptionPane.showMessageDialog(null, " [ Datos Actualizados ]");
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Error BD: " + e.getMessage());
        }
    }//GEN-LAST:event_jBueditarActionPerformed

