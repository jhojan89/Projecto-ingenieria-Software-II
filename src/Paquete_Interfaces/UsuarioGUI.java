public void BuscarUsuario(){
try {
            us= UsuarioBD.BuscarUsuario(jtfcodigousuario1.getText());
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "ERROR:" + e.getMessage());
        }
        if (us != null) {
            jtfcodigousuario1.setText(String.valueOf(us.getCodigousuario()));
            jtfnomusuario.setText(us.getNombre());
            jtfapellidoUsuario.setText(us.getApellido());
            jtfCedulausuario.setText(us.getCedula());
            jtfUsuario.setText(us.getUsuario());
            jtfTelefonoUsuario.setText(us.getTelefono());
            jtfpassworkUsuario.setText(us.getPasswork());
            jTextField1.setText(us.getTipoUsuario());
           
        jBueditar.setEnabled(true);
            jButtcancelar.setEnabled(true);
        
        
        
        }

        else {
            JOptionPane.showMessageDialog(null, "No exite");
           jBueditar.setEnabled(false);
            jtfcodigousuario1.setText("");
            jButtcancelar.setEnabled(false);
            jtfcodigousuario1.setEditable(true);
            }
        
}

    
